/// Test LSA.2: LSA header freshness comparison.
///
/// Purpose: Verify that `is_more_recent_than` correctly implements the
/// RFC 2328 Section 13.1 LSA comparison rules: higher sequence number wins,
/// then higher checksum, then age rules.
#[test]
fn lsa_2_freshness_comparison() {
    let h1 = LsaHeader {
        ls_age: 100,
        options: 0x02,
        ls_type: LsaType::Router,
        link_state_id: Ipv4Addr::new(1, 1, 1, 1),
        advertising_router: Ipv4Addr::new(1, 1, 1, 1),
        ls_sequence_number: 0x80000001u32 as i32,
        ls_checksum: 0,
        length: 36,
    };

    // Higher sequence number is more recent
    let mut h2 = h1.clone();
    h2.ls_sequence_number = 0x80000002u32 as i32;
    assert_eq!(
        h2.is_more_recent_than(&h1),
        std::cmp::Ordering::Greater,
        "higher sequence wins"
    );
    assert_eq!(
        h1.is_more_recent_than(&h2),
        std::cmp::Ordering::Less,
        "lower sequence loses"
    );

    // MaxAge LSA is more recent than non-MaxAge at same sequence
    let mut h3 = h1.clone();
    h3.ls_age = MAX_AGE;
    assert_eq!(
        h3.is_more_recent_than(&h1),
        std::cmp::Ordering::Greater,
        "MaxAge is more recent"
    );
}

/// Test NBMA.1: Non-Broadcast interface runs the Broadcast DR-election FSM.
///
/// Purpose: Verify that a router on an NBMA interface enters Waiting on
/// InterfaceUp (just like Broadcast), runs DR election on WaitTimer, and
/// — alone on the segment — becomes DR.
///
/// References: RFC 2328 §A.4.5 (non-broadcast networks use broadcast ISM)
#[test]
fn nbma_1_interface_runs_broadcast_fsm() {
    let mut iface = OspfInterface::new(
        "wan".to_string(),
        1,
        Ipv4Addr::new(10, 0, 0, 1),
        Ipv4Addr::new(255, 255, 255, 0),
        Ipv4Addr::UNSPECIFIED,
        NetworkType::NonBroadcast,
        Ipv4Addr::new(10, 0, 0, 1),
    );
    iface.priority = 1;
    iface.static_neighbors.push(StaticNeighbor {
        address: Ipv4Addr::new(10, 0, 0, 2),
        priority: 1,
    });

    // Must enter Waiting (not go straight to DROther or P2P)
    iface.handle_event(&InterfaceEvent::InterfaceUp);
    assert_eq!(iface.state, InterfaceState::Waiting);
    assert!(iface.wait_timer_expiry.is_some());

    // Wait timer fires: alone, so we become DR
    iface.handle_event(&InterfaceEvent::WaitTimer);
    assert_eq!(iface.state, InterfaceState::DR);
    assert_eq!(iface.dr, Ipv4Addr::new(10, 0, 0, 1));
}

