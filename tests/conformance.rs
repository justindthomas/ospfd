/// Test AUTH.1: Simple password authentication round-trip.
///
/// Purpose: Verify that an OSPF packet authenticated with a simple password
/// can be verified on receive with the same password, and fails with a
/// different password.
///
/// References: RFC 2328 Appendix D.3
///
/// Derived from: UNH IOL OSPF_Interop.1.2 (Authentication)
#[test]
fn auth_1_simple_password() {
    use ospfd::packet::auth::{apply_auth, verify_auth, AuthKey};

    // A minimal Hello packet (just the header, we don't care about body for
    // auth verification — the 24-byte header is enough).
    let mut packet = vec![0u8; 48];
    packet[0] = 2; // version
    packet[1] = 1; // hello
    packet[2] = 0;
    packet[3] = 48;

    let key = AuthKey::Simple(b"ospfpass".to_vec());
    let authed = apply_auth(packet, &key, 0);

    // Matching password passes
    assert!(verify_auth(&authed, &key));

    // Different password fails
    let wrong = AuthKey::Simple(b"wrongpw".to_vec());
    assert!(!verify_auth(&authed, &wrong));

    // Null auth fails (au_type mismatch)
    assert!(!verify_auth(&authed, &AuthKey::None));
}

/// Test AUTH.2: MD5 cryptographic authentication round-trip.
///
/// Purpose: Verify that an OSPF packet authenticated with MD5 HMAC-like
/// scheme (RFC 2328 Appendix D.4) can be verified with the correct key_id
/// and key, and fails if either is wrong.
#[test]
fn auth_2_md5_crypto() {
    use ospfd::packet::auth::{apply_auth, verify_auth, AuthKey};

    let mut packet = vec![0u8; 48];
    packet[0] = 2;
    packet[1] = 1;
    packet[2] = 0;
    packet[3] = 48;
    for i in 24..48 {
        packet[i] = i as u8;
    }

    let key = AuthKey::Md5 {
        key_id: 7,
        key: b"super-secret-key".to_vec(),
    };
    let authed = apply_auth(packet, &key, 12345);

    // Packet length in header does NOT include the 16-byte digest
    assert_eq!(u16::from_be_bytes([authed[2], authed[3]]), 48);
    // Total buffer IS extended
    assert_eq!(authed.len(), 48 + 16);

    assert!(verify_auth(&authed, &key));

    // Wrong key_id fails
    let wrong_kid = AuthKey::Md5 {
        key_id: 8,
        key: b"super-secret-key".to_vec(),
    };
    assert!(!verify_auth(&authed, &wrong_kid));

    // Wrong key fails
    let wrong_key = AuthKey::Md5 {
        key_id: 7,
        key: b"different-key".to_vec(),
    };
    assert!(!verify_auth(&authed, &wrong_key));
}

