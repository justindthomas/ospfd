/// Test NBMA.3: `should_form_adjacency` returns true for NBMA when DR/BDR.
///
/// NBMA shares the broadcast adjacency rule: only DR and BDR form full
/// adjacencies with DROther neighbors; two DROthers stop at 2-Way.
#[test]
fn nbma_3_adjacency_rule_matches_broadcast() {
    let mut iface = OspfInterface::new(
        "wan".to_string(),
        1,
        Ipv4Addr::new(10, 0, 0, 1),
        Ipv4Addr::new(255, 255, 255, 0),
        Ipv4Addr::UNSPECIFIED,
        NetworkType::NonBroadcast,
        Ipv4Addr::new(10, 0, 0, 1),
    );
    iface.state = InterfaceState::DR;
    iface.dr = Ipv4Addr::new(10, 0, 0, 1);

    let n = Neighbor::new(Ipv4Addr::new(2, 2, 2, 2), Ipv4Addr::new(10, 0, 0, 2));
    // We're DR, so we form adjacency with everyone in >=2-Way
    assert!(iface.should_form_adjacency(&n));

    // A DROther on a broadcast-style segment would not form adjacency
    // with another DROther.
    iface.state = InterfaceState::DROther;
    iface.dr = Ipv4Addr::new(10, 0, 0, 99);
    iface.bdr = Ipv4Addr::new(10, 0, 0, 98);
    let n2 = Neighbor::new(Ipv4Addr::new(3, 3, 3, 3), Ipv4Addr::new(10, 0, 0, 3));
    assert!(!iface.should_form_adjacency(&n2));
}
