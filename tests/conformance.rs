/// Test NSM.1: Normal adjacency formation on broadcast network.
///
/// Purpose: Verify that a neighbor progresses from Down to Full through the
/// standard state sequence when forming adjacency on a broadcast network.
///
/// References: RFC 2328 Section 10.3
///
/// Procedure:
///   1. Create a neighbor in Down state
///   2. Send HelloReceived -> expect Init
///   3. Send TwoWayReceived with adjacency -> expect ExStart
///   4. Send NegotiationDone -> expect Exchange
///   5. With empty request list, send ExchangeDone -> expect Full
#[test]
fn nsm_1_full_adjacency_formation() {
    let mut n = Neighbor::new(
        Ipv4Addr::new(2, 2, 2, 2),
        Ipv4Addr::new(10, 0, 0, 2),
    );
    assert_eq!(n.state, NeighborState::Down);

    assert_eq!(
        n.handle_event(&NeighborEvent::HelloReceived, false),
        Some(NeighborState::Init)
    );
    assert_eq!(
        n.handle_event(&NeighborEvent::TwoWayReceived, true),
        Some(NeighborState::ExStart)
    );
    assert_eq!(
        n.handle_event(&NeighborEvent::NegotiationDone, true),
        Some(NeighborState::Exchange)
    );
    assert_eq!(
        n.handle_event(&NeighborEvent::ExchangeDone, true),
        Some(NeighborState::Full)
    );
}

/// Test NSM.2: DR-Other neighbors stay in 2-Way.
///
/// Purpose: Verify that on a broadcast network, neighbors that are neither
/// DR nor BDR do not form full adjacencies — they stop at 2-Way.
///
/// References: RFC 2328 Section 10.3 (event 2-WayReceived)
#[test]
fn nsm_2_dr_other_stops_at_twoway() {
    let mut n = Neighbor::new(
        Ipv4Addr::new(3, 3, 3, 3),
        Ipv4Addr::new(10, 0, 0, 3),
    );
    n.handle_event(&NeighborEvent::HelloReceived, false);
    // Neither we nor the neighbor is DR/BDR
    n.handle_event(&NeighborEvent::TwoWayReceived, false);
    assert_eq!(n.state, NeighborState::TwoWay);
}

/// Test NSM.3: Sequence number mismatch restarts DD exchange.
///
/// Purpose: Verify that a SeqNumberMismatch event in Exchange or later
/// transitions the neighbor back to ExStart, clearing exchange state.
///
/// References: RFC 2328 Section 10.3 (event SeqNumberMismatch)
#[test]
fn nsm_3_seq_mismatch_restarts_dd() {
    let mut n = Neighbor::new(
        Ipv4Addr::new(2, 2, 2, 2),
        Ipv4Addr::new(10, 0, 0, 2),
    );
    n.handle_event(&NeighborEvent::HelloReceived, false);
    n.handle_event(&NeighborEvent::TwoWayReceived, true);
    n.handle_event(&NeighborEvent::NegotiationDone, true);
    assert_eq!(n.state, NeighborState::Exchange);

    // Simulate a mismatch
    n.handle_event(&NeighborEvent::SeqNumberMismatch, true);
    assert_eq!(n.state, NeighborState::ExStart);
}

/// Test NSM.4: Inactivity timer kills neighbor.
///
/// Purpose: Verify that when the RouterDeadInterval elapses without a Hello,
/// the neighbor transitions to Down from any state.
///
/// References: RFC 2328 Section 10.3 (event InactivityTimer)
#[test]
fn nsm_4_inactivity_timer_resets() {
    let mut n = Neighbor::new(
        Ipv4Addr::new(2, 2, 2, 2),
        Ipv4Addr::new(10, 0, 0, 2),
    );
    n.handle_event(&NeighborEvent::HelloReceived, false);
    n.handle_event(&NeighborEvent::TwoWayReceived, true);
    n.handle_event(&NeighborEvent::NegotiationDone, true);
    n.handle_event(&NeighborEvent::ExchangeDone, true);
    assert_eq!(n.state, NeighborState::Full);

    n.handle_event(&NeighborEvent::InactivityTimer, false);
    assert_eq!(n.state, NeighborState::Down);
    assert!(n.ls_request_list.is_empty());
    assert!(n.ls_retransmit_list.is_empty());
}

