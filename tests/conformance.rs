/// Test EXT.2: Type 2 external route (only metric counts).
///
/// Purpose: Verify that a Type 2 AS-External LSA computes its route cost
/// using the advertised metric only, ignoring the internal cost-to-ASBR.
///
/// References: RFC 2328 Section 16.4
#[test]
fn ext_2_type2_metric_ignores_asbr_cost() {
    let mut as_external = HashMap::new();
    let ext_lsa = Lsa {
        header: LsaHeader {
            ls_age: 0,
            options: 0x02,
            ls_type: LsaType::AsExternal,
            link_state_id: Ipv4Addr::new(10, 10, 10, 0),
            advertising_router: Ipv4Addr::new(9, 9, 9, 9),
            ls_sequence_number: INITIAL_SEQUENCE_NUMBER,
            ls_checksum: 0,
            length: 36,
        },
        body: LsaBody::AsExternal(AsExternalLsa {
            network_mask: Ipv4Addr::new(255, 255, 255, 0),
            metric_type_2: true, // Type 2
            metric: 42,
            forwarding_address: Ipv4Addr::UNSPECIFIED,
            external_route_tag: 0,
        }),
    };
    as_external.insert(ext_lsa.key(), ext_lsa);

    // Pretend the ASBR is reachable via cost 1000
    let mut router_paths = HashMap::new();
    router_paths.insert(
        Ipv4Addr::new(9, 9, 9, 9),
        (
            1000,
            ospfd::proto::spf::NextHop {
                address: Ipv4Addr::new(172, 30, 0, 1),
                sw_if_index: 5,
            },
        ),
    );

    let routes = calculate_external_routes(&as_external, &router_paths);
    let r = routes
        .iter()
        .find(|r| r.prefix == Ipv4Addr::new(10, 10, 10, 0))
        .expect("external route");

    // Type 2: cost should be exactly the external metric (42), not 1000+42
    assert_eq!(r.cost, 42);
    assert_eq!(r.next_hop, Ipv4Addr::new(172, 30, 0, 1));
    assert_eq!(r.sw_if_index, 5);
}

