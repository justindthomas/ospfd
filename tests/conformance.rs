// These tests validate that our packet layer produces and parses byte streams
// compatible with the OSPFv2 wire format (RFC 2328 Appendix A).

/// Test FMT.1: Parse a real FRR Hello packet
///
/// Purpose: Verify the parser correctly handles a Hello packet generated by
/// FRR 10.x in a production setting. Every field must parse to its expected
/// value and re-encoding must produce byte-identical output.
///
/// References: RFC 2328 Sections A.3.1, A.3.2
#[test]
fn fmt_1_parse_real_frr_hello() {
    // Captured from FRR 10.x running on Debian Bookworm, router-id 172.30.0.1,
    // sending to 224.0.0.5 on the WAN interface of an test environment.
    // IP header stripped; data starts at the OSPF header.
    let ospf_data: [u8; 48] = [
        0x02, 0x01, 0x00, 0x30, 0xac, 0x1e, 0x00, 0x01,
        0x00, 0x00, 0x00, 0x00, 0x4c, 0x1a, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0xff, 0xff, 0xff, 0x00, 0x00, 0x0a, 0x02, 0x01,
        0x00, 0x00, 0x00, 0x28, 0xac, 0x1e, 0x00, 0x01,
        0xac, 0x1e, 0x00, 0x02, 0xac, 0x1e, 0x00, 0x02,
    ];

    // Step 1: Verify the OSPF checksum
    assert!(
        verify_ospf_checksum(&ospf_data),
        "FRR Hello checksum did not validate"
    );

    // Step 2: Parse the packet
    let packet = OspfPacket::parse(&ospf_data).expect("parse FRR Hello");

    match packet {
        OspfPacket::Hello(header, hello) => {
            // Step 3: Verify header fields
            assert_eq!(header.version, 2, "OSPF version");
            assert_eq!(header.packet_length, 48, "packet length");
            assert_eq!(header.router_id, Ipv4Addr::new(172, 30, 0, 1));
            assert_eq!(header.area_id, Ipv4Addr::UNSPECIFIED, "area 0");
            assert_eq!(header.au_type, 0, "no authentication");

            // Step 4: Verify Hello body fields
            assert_eq!(hello.network_mask, Ipv4Addr::new(255, 255, 255, 0));
            assert_eq!(hello.hello_interval, 10);
            assert_eq!(hello.options.0, 0x02, "E bit set");
            assert_eq!(hello.router_priority, 1);
            assert_eq!(hello.router_dead_interval, 40);
            assert_eq!(hello.designated_router, Ipv4Addr::new(172, 30, 0, 1));
            assert_eq!(hello.backup_designated_router, Ipv4Addr::new(172, 30, 0, 2));
            assert_eq!(hello.neighbors.len(), 1);
            assert_eq!(hello.neighbors[0], Ipv4Addr::new(172, 30, 0, 2));

            // Step 5: Re-encode and verify byte-for-byte match
            let reencoded = OspfPacket::Hello(header, hello).encode();
            assert_eq!(
                reencoded.as_slice(),
                &ospf_data[..],
                "re-encoded Hello does not match original"
            );
        }
        _ => panic!("expected Hello packet"),
    }
}

