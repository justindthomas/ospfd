/// Test SPF.1: Next-hop resolution through a transit broadcast network.
///
/// Purpose: Verify that when SPF reaches a remote router via a transit
/// broadcast network (Network-LSA), routes inherit the correct next-hop
/// (the remote router's interface IP on the network) and outgoing
/// interface (our local sw_if_index attached to the network).
///
/// References: RFC 2328 Sections 16.1, 16.1.1
///
/// This is the bug we hit in our first live FRR interop test: routes were
/// being installed with next_hop=0.0.0.0 / sw_if_index=0 because we weren't
/// resolving next-hops through Network-LSAs.
#[test]
fn spf_1_transit_network_next_hop() {
    let mut lsdb = HashMap::new();

    // Us (R1) on broadcast network 172.30.0.0/24, R2 (FRR-like) is DR
    let r1 = Lsa {
        header: LsaHeader {
            ls_age: 0,
            options: 0x02,
            ls_type: LsaType::Router,
            link_state_id: Ipv4Addr::new(172, 30, 0, 2),
            advertising_router: Ipv4Addr::new(172, 30, 0, 2),
            ls_sequence_number: INITIAL_SEQUENCE_NUMBER,
            ls_checksum: 0,
            length: 36,
        },
        body: LsaBody::Router(RouterLsa {
            flags: 0,
            links: vec![RouterLink {
                link_id: Ipv4Addr::new(172, 30, 0, 1),  // DR's address
                link_data: Ipv4Addr::new(172, 30, 0, 2), // our address
                link_type: RouterLinkType::TransitNetwork,
                num_tos: 0,
                metric: 10,
            }],
        }),
    };
    lsdb.insert(r1.key(), r1);

    // R2 (DR, FRR-like) on the same network with a stub for an internal /32
    let r2 = Lsa {
        header: LsaHeader {
            ls_age: 0,
            options: 0x02,
            ls_type: LsaType::Router,
            link_state_id: Ipv4Addr::new(172, 30, 0, 1),
            advertising_router: Ipv4Addr::new(172, 30, 0, 1),
            ls_sequence_number: INITIAL_SEQUENCE_NUMBER,
            ls_checksum: 0,
            length: 48,
        },
        body: LsaBody::Router(RouterLsa {
            flags: 0,
            links: vec![
                RouterLink {
                    link_id: Ipv4Addr::new(172, 30, 0, 1),
                    link_data: Ipv4Addr::new(172, 30, 0, 1),
                    link_type: RouterLinkType::TransitNetwork,
                    num_tos: 0,
                    metric: 10,
                },
                RouterLink {
                    link_id: Ipv4Addr::new(10, 99, 0, 1),
                    link_data: Ipv4Addr::new(255, 255, 255, 255),
                    link_type: RouterLinkType::StubNetwork,
                    num_tos: 0,
                    metric: 0,
                },
            ],
        }),
    };
    lsdb.insert(r2.key(), r2);

    // Network-LSA for the transit network
    let net = Lsa {
        header: LsaHeader {
            ls_age: 0,
            options: 0x02,
            ls_type: LsaType::Network,
            link_state_id: Ipv4Addr::new(172, 30, 0, 1),
            advertising_router: Ipv4Addr::new(172, 30, 0, 1),
            ls_sequence_number: INITIAL_SEQUENCE_NUMBER,
            ls_checksum: 0,
            length: 32,
        },
        body: LsaBody::Network(NetworkLsa {
            network_mask: Ipv4Addr::new(255, 255, 255, 0),
            attached_routers: vec![
                Ipv4Addr::new(172, 30, 0, 2), // us
                Ipv4Addr::new(172, 30, 0, 1), // DR
            ],
        }),
    };
    lsdb.insert(net.key(), net);

    let interfaces = vec![SpfInterface {
        address: Ipv4Addr::new(172, 30, 0, 2),
        mask: Ipv4Addr::new(255, 255, 255, 0),
        sw_if_index: 1, // 'wan'
        cost: 10,
    }];

    let neighbors = vec![SpfNeighbor {
        router_id: Ipv4Addr::new(172, 30, 0, 1),
        address: Ipv4Addr::new(172, 30, 0, 1),
        sw_if_index: 1,
    }];

    let routes = calculate_spf(
        Ipv4Addr::new(172, 30, 0, 2),
        &lsdb,
        &interfaces,
        &neighbors,
    );

    // We should have a route to 10.99.0.1/32 with the correct next-hop
    let r = routes
        .iter()
        .find(|r| r.prefix == Ipv4Addr::new(10, 99, 0, 1))
        .expect("missing route to 10.99.0.1/32");

    assert_eq!(
        r.next_hop,
        Ipv4Addr::new(172, 30, 0, 1),
        "next-hop must be the remote router's address on the transit network"
    );
    assert_eq!(
        r.sw_if_index, 1,
        "outgoing interface must be our wan sw_if_index"
    );
    // Cost = our transit metric (10) + R2's stub metric (0) = 10
    assert_eq!(r.cost, 10);
}

